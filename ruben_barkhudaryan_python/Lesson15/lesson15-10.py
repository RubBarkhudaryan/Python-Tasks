print("Math module")
print("Rounding in math")

import math

print(math.ceil(5.000001))
print(math.ceil(5.99999))
print(math.ceil(-10.9))
print(math.ceil(-10.1))


print(math.floor(5.000001))
print(math.floor(5.99999))
print(math.floor(-10.9))
print(math.floor(-10.1))


print(math.trunc(5.000001))
print(math.trunc(5.99999))
print(math.trunc(-10.9))
print(math.trunc(-10.1))

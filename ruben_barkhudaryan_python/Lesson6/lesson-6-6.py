print("if...elif...else")
num = 3.4
if num > 0:
    print(num, "is a positive number")
elif num == 0:
    print(num, "is a zero")
else:
    print(num, "is a negative number")
num = 0
if num > 0:
    print(num, "is a positive number")
elif num == 0:
    print(num, "is a zero")
else:
    print(num, "is a negative number")
num = -3.4
if num > 0:
    print(num, "is a positive number")
elif num == 0:
    print(num, "is a zero")
else:
    print(num, "is a negative number")
